    /**
     * Fonction de labellisation des blocs, qui fait appel à la classe RuleBasedChunkClassifier
     */
    public void classify() {
        RuleBasedChunkClassifier classifier = new RuleBasedChunkClassifier(rulepath.getText(), new RTModelFactory());
        for (int i = 0; i < pdf.getPageList().size(); i++) {
            classifier.classify(pdf.getPageList().get(i).getAllChunkBlocks(SpatialOrdering.COLUMN_AWARE_MIXED_MODE));
        }
    }
